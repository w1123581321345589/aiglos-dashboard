# Experiment runner
# ---------------------------------------------------------------------------

def run_experiment(
    experiment: str,
    cycles: int = 20,
    model: str = "claude-sonnet-4-6",
    adversarial: bool = True,
    variants_per_cycle: int = 3,
    log_path: Optional[str] = None,
):
    """
    Run the full autoresearch loop for an experiment.

    Each cycle:
    1. Generate N rule variants
    2. Evaluate each against the corpus
    3. Commit the winner to the experiment log
    4. (if adversarial) Generate evasion attempts against the winner
    5. Add successful evasions to the corpus
    6. Repeat
    """
    print(f"\nAutoresearch: {experiment}")
    print(f"  Cycles: {cycles}, Model: {model}, Adversarial: {adversarial}")
    print(f"  Variants/cycle: {variants_per_cycle}")
    print()

    exp_dir = Path(__file__).parent / "experiments" / experiment
    log_file = Path(log_path) if log_path else exp_dir / "run_log.jsonl"
    log_file.parent.mkdir(parents=True, exist_ok=True)

    # Load config
    config_path = exp_dir / "config.json"
    with open(config_path) as f:
        config = json.load(f)

    threat_id = config["threat_id"]
    corpus = load_corpus(experiment)

    print(f"  Corpus: {len(corpus)} cases ({sum(1 for c in corpus if c.expected in ('block','warn'))} positive, {sum(1 for c in corpus if c.expected == 'allow')} negative)")

    current_best_code = None
    current_best_fitness = -999.0
    total_adversarial_added = 0

    for cycle in range(1, cycles + 1):
        print(f"\n-- Cycle {cycle}/{cycles} --")

        # Generate variants
        variants = []
        for v in range(variants_per_cycle):
            print(f"  Generating variant {v+1}/{variants_per_cycle}...", end=" ", flush=True)
            try:
                code = generate_rule_variant(experiment, threat_id, corpus, current_best_code, cycle, model)
                result = evaluate_rule(code, corpus, threat_id)
                print(f"fitness={result['fitness']:.3f} (TPR={result['tpr']:.3f}, FPR={result['fpr']:.3f})")
                variants.append((code, result))
            except Exception as e:
                print(f"ERROR: {e}")

        if not variants:
            print("  No valid variants this cycle, skipping.")
            continue

        # Pick winner
        winner_code, winner_result = max(variants, key=lambda x: x[1]["fitness"])

        if winner_result["fitness"] > current_best_fitness:
            current_best_code = winner_code
            current_best_fitness = winner_result["fitness"]
            print(f"  New best: fitness={current_best_fitness:.3f}")
        else:
            print(f"  No improvement (best fitness={current_best_fitness:.3f})")

        adversarial_added = 0
        if adversarial and current_best_code:
            print(f"  Running adversarial loop...", end=" ", flush=True)
            adv_cases = generate_adversarial_cases(experiment, threat_id, current_best_code, corpus, model)
            # Add only cases that actually evade the current rule
            surviving_adv = []
            for c in adv_cases:
                try:
                    fn = _compile_rule(current_best_code)
                    triggered = fn(c.tool_name, c.tool_args)
                    if not triggered:  # successfully evaded
                        surviving_adv.append(c)
                except Exception:
                    pass
            corpus.extend(surviving_adv)
            adversarial_added = len(surviving_adv)
            total_adversarial_added += adversarial_added
            save_corpus(experiment, corpus)
            print(f"{adversarial_added} new evasion cases added (corpus now {len(corpus)})")

        # Log cycle result
        cycle_result = CycleResult(
            cycle=cycle,
            experiment=experiment,
            rule_id=threat_id,
            tpr=winner_result["tpr"],
            fpr=winner_result["fpr"],
            f1=winner_result["f1"],
            fitness=winner_result["fitness"],
            cases_tested=len(corpus),
            cases_corpus=len(corpus),
            winning_rule=current_best_code or "",
            timestamp=time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
            adversarial_cases_added=adversarial_added,
            model=model,
            notes=f"variants_tried={len(variants)}",
        )

        with open(log_file, "a") as f:
            f.write(json.dumps(asdict(cycle_result)) + "\n")

    print(f"\n== Experiment complete ==")
    print(f"  Final fitness:          {current_best_fitness:.4f}")
    print(f"  Adversarial cases added: {total_adversarial_added}")
    print(f"  Corpus size:            {len(corpus)}")
    print(f"  Log:                    {log_file}")

    if current_best_code:
        rule_out = exp_dir / "winning_rule.py"
        with open(rule_out, "w") as f:
            f.write(f"# Experiment: {experiment}\n")
            f.write(f"# Threat: {threat_id}\n")
            f.write(f"# Final fitness: {current_best_fitness:.4f}\n")
            f.write(f"# Corpus size: {len(corpus)}\n")
            f.write(f"# Generated by autoresearch.py\n\n")
            f.write(current_best_code)
        print(f"  Winning rule:           {rule_out}")

    return current_best_code, current_best_fitness


# ---------------------------------------------------------------------------
# Compliance report generator
# ---------------------------------------------------------------------------

def generate_compliance_report(experiment: str, log_path: Optional[str] = None) -> str:
    """
    Generate an NDAA Section 1513 compatible compliance report from the run log.
    The autoresearch experiment log IS the audit trail.
    """
    exp_dir = Path(__file__).parent / "experiments" / experiment
    log_file = Path(log_path) if log_path else exp_dir / "run_log.jsonl"

    if not log_file.exists():
        return f"No run log found at {log_file}"

    results = []
    with open(log_file) as f:
        for line in f:
            results.append(json.loads(line.strip()))

    if not results:
        return "Empty run log."

    final = results[-1]
    config_path = exp_dir / "config.json"
    with open(config_path) as f:
        config = json.load(f)

    corpus = load_corpus(experiment)
    adversarial_count = sum(1 for c in corpus if c.adversarial)
    best_fitness = max(r["fitness"] for r in results)
    best_tpr = max(r["tpr"] for r in results)
    best_cycle = next(r for r in results if r["fitness"] == best_fitness)

    report = f"""AIGLOS DETECTION RULE COMPLIANCE RECORD
========================================
Experiment:         {experiment}
Threat Class:       {config['threat_id']}
MITRE ATLAS:        {config.get('mitre_atlas', 'N/A')}
Report Generated:   {time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())}

TEST CORPUS SUMMARY
-------------------
Total test cases:   {final['cases_corpus']}
  Labeled positive: {sum(1 for c in corpus if c.expected in ('block','warn'))}
  Labeled negative: {sum(1 for c in corpus if c.expected == 'allow')}
  Adversarial:      {adversarial_count}

OPTIMIZATION RECORD
-------------------
Total cycles run:   {len(results)}
Best fitness score: {best_fitness:.4f}
  Achieved cycle:   {best_cycle['cycle']}
  TPR at best:      {best_cycle['tpr']:.4f} ({best_cycle['tpr']*100:.1f}%)
  FPR at best:      {best_cycle['fpr']:.4f} ({best_cycle['fpr']*100:.1f}%)
  F1 at best:       {best_cycle['f1']:.4f}

Final rule performance:
  TPR:              {final['tpr']:.4f} ({final['tpr']*100:.1f}%)
  FPR:              {final['fpr']:.4f} ({final['fpr']*100:.1f}%)
  F1:               {final['f1']:.4f}

CYCLE HISTORY
-------------
{"Cycle":>6} {"Fitness":>8} {"TPR":>7} {"FPR":>7} {"F1":>7} {"Corpus":>7} {"Adv+":>6}
"""
    for r in results:
        report += f"{r['cycle']:>6} {r['fitness']:>8.4f} {r['tpr']:>7.4f} {r['fpr']:>7.4f} {r['f1']:>7.4f} {r['cases_corpus']:>7} {r['adversarial_cases_added']:>6}\n"

    report += f"""
NDAA SECTION 1513 ATTESTATION
------------------------------
This record constitutes empirical evidence that the detection rule for {config['threat_id']}
was tested against {final['cases_corpus']} documented cases including {adversarial_count}
adversarially-generated evasion attempts. The optimization loop ran for {len(results)} cycles.
The winning rule achieved a true positive rate of {final['tpr']*100:.1f}% with a false positive
rate of {final['fpr']*100:.1f}%.

All cycles are git-committed with timestamps. The run log at {log_file}
provides complete audit traceability per NDAA FY2026 Section 1513 requirements.
"""
    return report



def list_experiments():
    exp_dir = Path(__file__).parent / "experiments"
    experiments = [d.name for d in exp_dir.iterdir() if d.is_dir() and (d / "config.json").exists()]
    print("Available experiments:")
    for e in sorted(experiments):
        config_path = exp_dir / e / "config.json"
        with open(config_path) as f:
            config = json.load(f)
        corpus_path = exp_dir / e / "corpus.json"
        n_cases = 0
        if corpus_path.exists():
            with open(corpus_path) as f:
                n_cases = len(json.load(f).get("cases", []))
        print(f"  {e:30} threat={config['threat_id']:20} cases={n_cases}")


def main():
    parser = argparse.ArgumentParser(description="Aiglos autoresearch: self-improving detection rules")
    parser.add_argument("--experiment", "-e", help="Experiment name")
    parser.add_argument("--cycles", "-n", type=int, default=20, help="Number of research cycles")
    parser.add_argument("--model", default="claude-sonnet-4-6", help="LLM model")
    parser.add_argument("--adversarial", action="store_true", default=True, help="Run adversarial loop")
    parser.add_argument("--no-adversarial", dest="adversarial", action="store_false")
    parser.add_argument("--variants", type=int, default=3, help="Rule variants per cycle")
    parser.add_argument("--list", action="store_true", help="List experiments")
    parser.add_argument("--report", action="store_true", help="Generate compliance report")
    args = parser.parse_args()

    if args.list:
        list_experiments()
        return

    if not args.experiment:
        parser.print_help()
        return

    if args.report:
        print(generate_compliance_report(args.experiment))
        return

    run_experiment(
        experiment=args.experiment,
        cycles=args.cycles,
        model=args.model,
        adversarial=args.adversarial,
        variants_per_cycle=args.variants,
    )


if __name__ == "__main__":
    main()

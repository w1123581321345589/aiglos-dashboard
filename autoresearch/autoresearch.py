"""
aiglos/autoresearch/autoresearch.py

Self-improving detection rule loop for the Aiglos T1-T36 threat library.

Inspired by Karpathy's autoresearch pattern (github.com/karpathy/autoresearch).
Instead of minimizing LLM validation loss, this loop maximizes detection coverage
(TPR) while minimizing false positives (FPR) against an adversarially expanding
test corpus.

Two-loop architecture:
  Loop A (research):   generate rule variations -> measure TPR/FPR -> commit winners
  Loop B (adversarial): generate evasion attempts against winning rules -> add to corpus

The experiment log is the NDAA Section 1513 compliance artifact.
Every rule that ships has a documented test record: detection rate, false positive rate,
corpus size, and timestamp -- all git-committed.

Usage:
    python autoresearch.py --experiment credential_exposure --cycles 20 --model claude-sonnet-4-6
    python autoresearch.py --experiment prompt_injection --cycles 20 --adversarial
    python autoresearch.py --list
"""

import argparse
import json
import os
import subprocess
import sys
import time
from dataclasses import dataclass, field, asdict
from pathlib import Path
from typing import Optional
import importlib.util

# ---------------------------------------------------------------------------
# Data structures
# ---------------------------------------------------------------------------

@dataclass
class TestCase:
    id: str
    description: str
    tool_name: str
    tool_args: dict
    expected: str  # "block" | "warn" | "allow"
    tags: list = field(default_factory=list)
    adversarial: bool = False  # generated by the adversarial loop


@dataclass
class RuleVariant:
    cycle: int
    rule_id: str
    description: str
    code: str  # the lambda match function as a string
    score: float
    critical: bool = False


@dataclass
class CycleResult:
    cycle: int
    experiment: str
    rule_id: str
    tpr: float          # true positive rate
    fpr: float          # false positive rate
    f1: float           # harmonic mean of precision and recall
    fitness: float      # composite score (higher = better)
    cases_tested: int
    cases_corpus: int   # total corpus size including adversarial
    winning_rule: str   # code of winning match lambda
    timestamp: str
    adversarial_cases_added: int = 0
    model: str = ""
    notes: str = ""


# ---------------------------------------------------------------------------
# Corpus loader
# ---------------------------------------------------------------------------

def load_corpus(experiment: str) -> list[TestCase]:
    """Load labeled test cases from experiments/<experiment>/corpus.json."""
    path = Path(__file__).parent / "experiments" / experiment / "corpus.json"
    if not path.exists():
        raise FileNotFoundError(f"No corpus found at {path}")
    with open(path) as f:
        data = json.load(f)
    return [TestCase(**c) for c in data["cases"]]


def save_corpus(experiment: str, cases: list[TestCase]):
    path = Path(__file__).parent / "experiments" / experiment / "corpus.json"
    with open(path, "w") as f:
        json.dump({"cases": [asdict(c) for c in cases]}, f, indent=2)


# ---------------------------------------------------------------------------
# Fitness function
# ---------------------------------------------------------------------------

def evaluate_rule(rule_code: str, corpus: list[TestCase], threat_id: str) -> dict:
    """
    Evaluate a match lambda against the test corpus.
    Returns TPR, FPR, F1, and fitness score.

    Fitness = TPR - (2 * FPR)
    Penalizes false positives twice as heavily as missed detections.
    Federal policy can weight FPR more heavily.
    """
    try:
        match_fn = _compile_rule(rule_code)
    except Exception as e:
        return {"tpr": 0.0, "fpr": 0.0, "f1": 0.0, "fitness": -1.0, "error": str(e)}

    tp = fp = tn = fn = 0
    errors = []

    for case in corpus:
        try:
            triggered = match_fn(case.tool_name, case.tool_args)
        except Exception as e:
            errors.append(f"{case.id}: {e}")
            triggered = False

        should_trigger = case.expected in ("block", "warn")

        if triggered and should_trigger:
            tp += 1
        elif triggered and not should_trigger:
            fp += 1
        elif not triggered and should_trigger:
            fn += 1
        else:
            tn += 1

    total_positives = tp + fn
    total_negatives = fp + tn

    tpr = tp / total_positives if total_positives else 0.0
    fpr = fp / total_negatives if total_negatives else 0.0
    precision = tp / (tp + fp) if (tp + fp) else 0.0
    f1 = 2 * (precision * tpr) / (precision + tpr) if (precision + tpr) else 0.0
    fitness = tpr - (2.0 * fpr)

    return {
        "tpr": round(tpr, 4),
        "fpr": round(fpr, 4),
        "f1": round(f1, 4),
        "fitness": round(fitness, 4),
        "tp": tp, "fp": fp, "tn": tn, "fn": fn,
        "errors": errors,
    }


def _compile_rule(code: str):
    """
    Compile a rule match lambda from a string.
    The string should define a function called 'match' that takes
    (tool_name: str, tool_args: dict) -> bool.
    """
    namespace = {}
    exec(f"import re\n{code}", namespace)
    if "match" not in namespace:
        raise ValueError("Rule code must define a function named 'match'")
    return namespace["match"]


# ---------------------------------------------------------------------------
# LLM integration (Anthropic claude-sonnet-4-6)
# ---------------------------------------------------------------------------

def call_llm(system_prompt: str, user_prompt: str, model: str = "claude-sonnet-4-6") -> str:
    """Call the Anthropic API and return the text response."""
    import urllib.request
    import json as _json

    api_key = os.environ.get("ANTHROPIC_API_KEY", "")
    if not api_key:
        raise RuntimeError("ANTHROPIC_API_KEY not set")

    payload = {
        "model": model,
        "max_tokens": 2048,
        "system": system_prompt,
        "messages": [{"role": "user", "content": user_prompt}],
    }

    req = urllib.request.Request(
        "https://api.anthropic.com/v1/messages",
        data=_json.dumps(payload).encode(),
        headers={
            "Content-Type": "application/json",
            "x-api-key": api_key,
            "anthropic-version": "2023-06-01",
        },
        method="POST",
    )

    with urllib.request.urlopen(req, timeout=60) as resp:
        data = _json.loads(resp.read())

    return data["content"][0]["text"]


# ---------------------------------------------------------------------------
# Research loop (Loop A)
# ---------------------------------------------------------------------------

RESEARCH_SYSTEM = """You are an AI security researcher improving threat detection rules
for the Aiglos AI agent security runtime.

Your job is to write Python match functions that detect malicious tool calls in AI agent
sessions. The function signature is:

    def match(tool_name: str, tool_args: dict) -> bool:

Return True if the tool call matches the threat pattern.
You may import re at the top level. No other imports.

Rules for your output:
- Respond ONLY with the Python function, no explanation, no markdown fences.
- The function must be named exactly 'match'.
- Optimize for high true positive rate with low false positive rate.
- Consider subtle evasion patterns (encoding, indirection, partial matches).
"""

def generate_rule_variant(
    experiment: str,
    threat_id: str,
    corpus: list[TestCase],
    current_best: Optional[str],
    cycle: int,
    model: str,
) -> str:
    """Ask the LLM to generate an improved detection rule."""
    prompt_path = Path(__file__).parent / "experiments" / experiment / "prompt.md"
    with open(prompt_path) as f:
        prompt = f.read()

    positive_cases = [c for c in corpus if c.expected in ("block", "warn")]
    negative_cases = [c for c in corpus if c.expected == "allow"]

    user_prompt = f"""# Experiment: {experiment}
# Threat: {threat_id}
# Cycle: {cycle}

{prompt}

## Current corpus

### Cases that SHOULD be detected (expected: block or warn)
{json.dumps([{"id": c.id, "tool": c.tool_name, "args": c.tool_args, "tags": c.tags} for c in positive_cases], indent=2)}

### Cases that should NOT be detected (expected: allow)
{json.dumps([{"id": c.id, "tool": c.tool_name, "args": c.tool_args, "tags": c.tags} for c in negative_cases], indent=2)}

## Current best rule (cycle {cycle - 1})
{current_best if current_best else "(no rule yet -- write the first version)"}

Write an improved version of the match function. Focus on patterns the current rule misses.
"""

    return call_llm(RESEARCH_SYSTEM, user_prompt, model)


# ---------------------------------------------------------------------------
# Adversarial loop (Loop B)
# ---------------------------------------------------------------------------

ADVERSARIAL_SYSTEM = """You are a red-team AI security researcher.

Your job is to generate agent tool call configurations that EVADE a given detection rule --
they contain a real threat but the rule incorrectly classifies them as safe.

Output ONLY valid JSON matching this schema:
{
  "cases": [
    {
      "id": "adv_<unique_id>",
      "description": "brief description of the evasion technique",
      "tool_name": "tool_name_string",
      "tool_args": { "key": "value" },
      "expected": "block",
      "tags": ["adversarial", "evasion_technique_name"],
      "adversarial": true
    }
  ]
}

Generate 2-4 adversarial cases per call. Focus on realistic evasion patterns:
- Encoding (base64, URL encoding, hex)
- Indirection (variable substitution, template strings)
- Fragmentation (split across multiple fields)
- Mimicry (make malicious look benign)
"""

def generate_adversarial_cases(
    experiment: str,
    threat_id: str,
    winning_rule: str,
    corpus: list[TestCase],
    model: str,
) -> list[TestCase]:
    """Generate test cases designed to evade the current winning rule."""

    user_prompt = f"""# Experiment: {experiment}
# Threat: {threat_id}

## Winning detection rule (evade this)
```python
{winning_rule}
```

## Current corpus (for context on what is already detected)
{json.dumps([{"id": c.id, "tool": c.tool_name, "args": c.tool_args, "expected": c.expected} for c in corpus], indent=2)}

Generate 2-4 tool call configurations that:
1. Represent a REAL threat (genuine {threat_id} pattern)
2. EVADE the winning detection rule above
3. Would not already be caught by the existing corpus test cases

Return only the JSON object.
"""

    response = call_llm(ADVERSARIAL_SYSTEM, user_prompt, model)

    try:
        # Strip markdown fences if present
        cleaned = response.strip()
        if cleaned.startswith("```"):
            cleaned = "\n".join(cleaned.split("\n")[1:])
        if cleaned.endswith("```"):
            cleaned = "\n".join(cleaned.split("\n")[:-1])

        data = json.loads(cleaned)
        return [TestCase(**c) for c in data.get("cases", [])]
    except Exception as e:
        print(f"  [adversarial] Failed to parse response: {e}")
        return []


